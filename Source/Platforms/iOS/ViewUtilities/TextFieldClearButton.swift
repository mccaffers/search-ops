import SwiftUI

struct TextFieldClearButton: ViewModifier {
    @Binding var text: String
    
    func body(content: Content) -> some View {
        HStack {
            content
            
            if !text.isEmpty {
                Button(
                    action: { self.text = "" },
                    label: {
                        Image(systemName: "delete.left")
#if os(iOS)
                            .foregroundColor(Color(UIColor.opaqueSeparator))
                      #endif
                    }
                )
            }
        }
    }
}

